package com.spring.elastic.search.model;

public class Reader {
	
	private long id;
	private String name;
	private String mobile;
	private String email;
	private String address;
	private String dateOfBirth;
	private String dateOfMembership;
	private String referralContactName;
	private String referralContactMobile;
	private String referralContactEmail;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getDateOfBirth() {
		return dateOfBirth;
	}
	public void setDateOfBirth(String dateOfBirth) {
		this.dateOfBirth = dateOfBirth;
	}
	public String getDateOfMembership() {
		return dateOfMembership;
	}
	public void setDateOfMembership(String dateOfMembership) {
		this.dateOfMembership = dateOfMembership;
	}
	public String getReferralContactName() {
		return referralContactName;
	}
	public void setReferralContactName(String referralContactName) {
		this.referralContactName = referralContactName;
	}
	public String getReferralContactMobile() {
		return referralContactMobile;
	}
	public void setReferralContactMobile(String referralContactMobile) {
		this.referralContactMobile = referralContactMobile;
	}
	public String getReferralContactEmail() {
		return referralContactEmail;
	}
	public void setReferralContactEmail(String referralContactEmail) {
		this.referralContactEmail = referralContactEmail;
	}
	
	

}
